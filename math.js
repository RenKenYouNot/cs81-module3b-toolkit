// n = 5 for all of the functions
// function to double number
function double(n) {
  return n * 2;
}
// Function to square a number
function square(n) {
  return n * n;
}
// Function to check if the remainder is even
function isEven(n) {
  return n % 2 === 0; // This returns true if the remainder of 'n' is even
}
// Function to check if the remainder is odd
function isOdd(n) {
  return n % 2 !== 0; // This returns true if the remainder of 'n' is odd
}
// Function to subtract 5 from a number
function subtractFive(n) {
  return n - 5;
}




